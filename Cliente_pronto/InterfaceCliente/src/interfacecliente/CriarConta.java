    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        Clientes cliente = new Clientes();
        String nome, cpf, senha;
        nome = txtNome.getText();
        cpf = txtCpf.getText();
        senha = txtSenha.getText();
        if(nome.equals("") || cpf.equals("") || senha.equals("")){
            JOptionPane.showMessageDialog(null, "Coloque seus dados corretamente", "", JOptionPane.ERROR_MESSAGE);
        }
        else{
            UsuarioDAO dao = new UsuarioDAO();

            boolean encontrou = dao.verificaConta(new Pessoa(txtCpf.getText(), txtSenha.getText()));
            if(encontrou)
                JOptionPane.showMessageDialog(null, "CPF j√° cadastrado!", "", JOptionPane.ERROR_MESSAGE);
            else{
                Pessoa pessoa = new Pessoa(cpf, senha, nome);
                cliente.inserir(pessoa);
                JOptionPane.showMessageDialog(null, "Cadastrado", "", JOptionPane.INFORMATION_MESSAGE);
            }
            }
    }//GEN-LAST:event_btnCadastrarActionPerformed

